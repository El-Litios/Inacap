    private void ComboCategoria(){
       DefaultComboBoxModel combo = new DefaultComboBoxModel();
        Control_CategoriaLibro ccal=new Control_CategoriaLibro();
        List<Categoria> lista=ccal.ComboBoxCate();
        for(Categoria C:lista){
            C = new Categoria(C.getCod_cate(), C.getNom_cate());
            combo.addElement(C);
        }
        ComboCate.setModel(combo);
    }
    
    private void ComboLibros(){
       DefaultComboBoxModel combo = new DefaultComboBoxModel();
        Control_AutorLibro ca=new Control_AutorLibro();
        List<Libros> lista=ca.ComboBoxLibros();
        for(Libros L:lista){
            L = new Libros(L.getCod_libro(), L.getTitulo_libro());
            combo.addElement(L);
        }
        ComboLibro.setModel(combo);
    }
