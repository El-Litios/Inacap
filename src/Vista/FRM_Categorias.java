    public void LlenarTablaCateLibro(String dato){
    tabla.setColumnCount(0);
        tabla.addColumn("ISBN de Libro");
        tabla.addColumn("Titulo de Libro");
        tabla.addColumn("Numero de Paginas");
        tabla.addColumn("Precio de Libro");
        tabla.addColumn("Categoria");
        List<CategoriaLibro> lista=crc.ListarCategLibro(dato);
        RegistrosC.setText("NÂ° DE LIBROS CON CATEGORIAS= "+lista.size());
        tabla.setNumRows(lista.size());
        for (int i = 0; i < lista.size(); i++) {
            tabla.setValueAt(lista.get(i).getIsbn_libro(), i, 0);
            tabla.setValueAt(lista.get(i).getTitulo_libro(), i, 1);
            tabla.setValueAt(lista.get(i).getPag_libro(), i, 2);
            tabla.setValueAt(lista.get(i).getPrecio_libro(), i, 3);
            tabla.setValueAt(lista.get(i).getNom_cate(), i, 4);
        }
        TablaCategorias.setModel(tabla);
    }
    
