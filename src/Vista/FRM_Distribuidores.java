    private void GuardarDistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarDistActionPerformed
        PaisDist Pdist=(PaisDist)CPais.getSelectedItem();
        Comuna com=(Comuna)CComuna.getSelectedItem();
        if (RutDist.getText().equals("") || NombreDist.getText().equals("") || DirecDist.getText().equals("") 
            ||  TelefonoDist.getText().equals("") || ServDist.getText().equals("") || EmailDist.getText().equals("") 
            ||   ContacDist.getText().equals("")  ){
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR TODOS LOS DATOS!!!", "La biblioteca",JOptionPane.OK_OPTION+JOptionPane.INFORMATION_MESSAGE);
            RutDist.grabFocus();
        }else{
            if (sswitch==0) crd.insertarDistribuidores(RutDist.getText(), NombreDist.getText(), DirecDist.getText(), 
                    TelefonoDist.getText(), Integer.parseInt(ServDist.getText()), EmailDist.getText(), ContacDist.getText(), com.getCod_comuna(), Pdist.getCod_pais());
            
            else crd.editarDistribuidores(codDist, RutDist.getText(), NombreDist.getText(), DirecDist.getText(), 
                    TelefonoDist.getText(), Integer.parseInt(ServDist.getText()), EmailDist.getText(), ContacDist.getText(), com.getCod_comuna(), Pdist.getCod_pais());
            PanelDist.setSelectedIndex(1);  
            JOptionPane.showMessageDialog(this, "OPERACION REALIZADA", "LA BIBLIOTECA",JOptionPane.OK_OPTION+JOptionPane.INFORMATION_MESSAGE);   
        }   
        RutDist.setText("");
        NombreDist.setText("");
        DirecDist.setText("");
        TelefonoDist.setText("");
        ServDist.setText("");
        EmailDist.setText("");
        ContacDist.setText("");
        LlenarTabla1Dist("");
        sswitch=0;
      
    }//GEN-LAST:event_GuardarDistActionPerformed

