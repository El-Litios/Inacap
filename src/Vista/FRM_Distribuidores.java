    public void LlenarTabla1Dist(String dato){
    tabla.setColumnCount(0);
        tabla.addColumn("Codigo");
        tabla.addColumn("Rut");
        tabla.addColumn("Nombre");
        tabla.addColumn("Direccion");
        tabla.addColumn("Telefono");
        tabla.addColumn("Tiempo de Servicio");
        tabla.addColumn("Email");
        tabla.addColumn("Contacto Encargado");
        tabla.addColumn("Pais de Origen");
        tabla.addColumn("Comuna");
        List<Distribuidor> lista=crd.ListarDistribuidores(dato);
        tabla.setNumRows(lista.size());
        registros.setText("CANTIDAD DE REGISTROS= "+lista.size());
        for (int i = 0; i < lista.size(); i++) {
            tabla.setValueAt(lista.get(i).getCod_dist(), i, 0);
            tabla.setValueAt(lista.get(i).getRut_dist(), i, 1);
            tabla.setValueAt(lista.get(i).getNom_dist(), i, 2);
            tabla.setValueAt(lista.get(i).getDir_dist(), i, 3);
            tabla.setValueAt(lista.get(i).getTel_dist(), i, 4);
            tabla.setValueAt(lista.get(i).getTiempo_dist(), i, 5);
            tabla.setValueAt(lista.get(i).getEm_dist(), i, 6);
            tabla.setValueAt(lista.get(i).getContacto_dist(), i, 7);
            tabla.setValueAt(lista.get(i).getNom_pais(), i, 8);
            tabla.setValueAt(lista.get(i).getNom_comuna(), i, 9);
        }
        TablaDist.setModel(tabla);
        TablaDist.getColumnModel().getColumn(0).setMaxWidth(0);
        TablaDist.getColumnModel().getColumn(0).setMinWidth(0);
        TablaDist.getColumnModel().getColumn(0).setPreferredWidth(0);
    }
    
     private void ComboPais(){
       DefaultComboBoxModel combo = new DefaultComboBoxModel();
        Control_Distribuidor cod=new Control_Distribuidor();
        List<PaisDist> lista=cod.comboBoxPais();
        for(PaisDist P:lista){
            P = new PaisDist(P.getCod_pais(), P.getNom_pais());
            combo.addElement(P);
        }
        CPais.setModel(combo);
    }
    
    private void ComboComuna(){
       DefaultComboBoxModel combo = new DefaultComboBoxModel();
        Control_Distribuidor cod=new Control_Distribuidor();
        List<Comuna> lista=cod.comboBoxComunas();
        for(Comuna C:lista){
            C = new Comuna(C.getCod_comuna(), C.getNom_comuna());
            combo.addElement(C);
        }
        CComuna.setModel(combo);
    }
    
