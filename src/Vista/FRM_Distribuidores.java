    private void EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarActionPerformed
        if (filaDist>-1) {
            codDist=Integer.parseInt(String.valueOf(TablaDist.getValueAt(filaDist, 0)));
            RutDist.setText(String.valueOf(TablaDist.getValueAt(filaDist, 1)));
            NombreDist.setText(String.valueOf(TablaDist.getValueAt(filaDist, 2)));
            DirecDist.setText(String.valueOf(TablaDist.getValueAt(filaDist, 3)));
            TelefonoDist.setText(String.valueOf(TablaDist.getValueAt(filaDist, 4)));
            ServDist.setText(String.valueOf(TablaDist.getValueAt(filaDist, 5)));
            EmailDist.setText(String.valueOf(TablaDist.getValueAt(filaDist, 6)));
            ContacDist.setText(String.valueOf(TablaDist.getValueAt(filaDist, 7)));
            sswitch=1;
            PanelDist.setSelectedIndex(1);
        } else {
           JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UNA FILA","ADVERTENCIA",JOptionPane.OK_OPTION+JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_EditarActionPerformed

