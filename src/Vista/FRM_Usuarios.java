    public void LlenarTablaUsuario(String dato){
    tabla.setColumnCount(0);
        tabla.addColumn("Codigo");
        tabla.addColumn("Usuario");
        tabla.addColumn("Contraseña");
        tabla.addColumn("Rol");
        List<Usuarios> lista=cru.listar(dato);
        tabla.setNumRows(lista.size());
        cantUsuarios.setText("CANTIDAD DE REGISTROS= "+lista.size());
        for (int i = 0; i < lista.size(); i++) {
            tabla.setValueAt(lista.get(i).getCod_usu(), i, 0);
            tabla.setValueAt(lista.get(i).getNom_usu(), i, 1);
            tabla.setValueAt(lista.get(i).getPass_usu(), i, 2);
            tabla.setValueAt(lista.get(i).getNom_cargo(), i, 3);
        }
        TablaUsuarios.setModel(tabla);
        TablaUsuarios.getColumnModel().getColumn(0).setMaxWidth(0);
        TablaUsuarios.getColumnModel().getColumn(0).setMinWidth(0);
        TablaUsuarios.getColumnModel().getColumn(0).setPreferredWidth(0);
        
        TablaUsuarios.getColumnModel().getColumn(2).setMaxWidth(0);
        TablaUsuarios.getColumnModel().getColumn(2).setMinWidth(0);
        TablaUsuarios.getColumnModel().getColumn(2).setPreferredWidth(0);
    }
    
    private void ComboRol(){
       DefaultComboBoxModel combo = new DefaultComboBoxModel();
        Control_Usuario cu=new Control_Usuario();
        List<Cargo> lista=cu.ComboBoxCargosU();
        for(Cargo c:lista){
            c = new Cargo(c.getCod_cargo(), c.getNom_cargo());
            combo.addElement(c);
        }
        ComboRol.setModel(combo);
    }
    
