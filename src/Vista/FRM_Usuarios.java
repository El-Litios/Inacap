    private void GuardarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarUsuarioActionPerformed
        Cargo c=(Cargo)ComboRol.getSelectedItem();
        String pass=String.copyValueOf(Password.getPassword());
        if (Usuario.getText().equals("") && pass.equals("")){
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR TODOS LOS DATOS", "La biblioteca",JOptionPane.OK_OPTION+JOptionPane.INFORMATION_MESSAGE);
        }else{
            if (sswitch==0) cru.InsertarUsuarios(Usuario.getText(),  pass, c.getCod_cargo());
            else cru.EditarUsuarios(codu, Usuario.getText(),  pass, c.getCod_cargo());
        }
        Usuario.setText("");
        Password.setText("");
        LlenarTablaUsuario("");
        sswitch=0;
        Panel.setSelectedIndex(0);
        JOptionPane.showMessageDialog(this, "OPERACION REALIZADA", "EL MUSEO",JOptionPane.OK_OPTION+JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_GuardarUsuarioActionPerformed


