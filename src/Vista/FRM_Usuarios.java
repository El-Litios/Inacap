    private void EditarUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarUsuariosActionPerformed
        if (filau>-1) {
            codu=Integer.parseInt(String.valueOf(TablaUsuarios.getValueAt(filau, 0)));
            Usuario.setText(String.valueOf(TablaUsuarios.getValueAt(filau, 1)));
            sswitch=1;
            Panel.setSelectedIndex(1);
        } else {
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UNA FILA","ADVERTENCIA",JOptionPane.OK_OPTION+JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_EditarUsuariosActionPerformed

