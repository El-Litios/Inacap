    private void EliminarNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarNActionPerformed
        if (filaN>-1) {
            int respuesta=JOptionPane.showConfirmDialog(this, "¿REALMENTE DESEA ELEMINAR EL REGISTRO? NO VOLVERA A RECUPERAR EL REGISTRO..."+" "
                +String.valueOf(TablaNacionalidad.getValueAt(filaN, 1)),"ADVERTENCIA",JOptionPane.YES_NO_OPTION+JOptionPane.WARNING_MESSAGE);
            if (respuesta==0) {
                cra.EliminarNacion(Integer.parseInt(String.valueOf(TablaNacionalidad.getValueAt(filaN, 0))));
                LlenarTablaNac("");
                JOptionPane.showMessageDialog(this, "NACIONALIDAD ELIMINADA", "LA BIBLIOTECA",JOptionPane.OK_OPTION+JOptionPane.INFORMATION_MESSAGE);
                filaN=-1;
            }
        } else {
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UNA FILA","ADVERTENCIA",JOptionPane.OK_OPTION+JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_EliminarNActionPerformed

    private void GuardarNacion1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarNacion1ActionPerformed
        PaisAutor pa=(PaisAutor)ComboPais.getSelectedItem();
        if (DescNac.getText().equals("") ){
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR EL DATO", "La biblioteca",JOptionPane.OK_OPTION+JOptionPane.INFORMATION_MESSAGE);
            DescNac.grabFocus();
        }else{
        if (sswitch==0) cra.IngresarNacion(DescNac.getText(), pa.getCod_pais());
        
        }
        PanelNac.setSelectedIndex(0);
        DescNac.setText("");
        LlenarTablaNac("");
        JOptionPane.showMessageDialog(this, "OPERACION REALIZADA", "LA BIBLIOTECA",JOptionPane.OK_OPTION+JOptionPane.INFORMATION_MESSAGE);
        sswitch=0;
    }//GEN-LAST:event_GuardarNacion1ActionPerformed

