    public void LlenarTablaNac(String dato){
    tabla.setColumnCount(0);
        tabla.addColumn("Codigo");
        tabla.addColumn("Descripcion Nacionalidad");
        tabla.addColumn("Pais Asociado");
        List<NacionAutor> lista=cra.listaNacionalidad(dato);
        RegistrosEdit.setText("N° DE NACIONALDADES GUARDADAS= "+lista.size());
        tabla.setNumRows(lista.size());
        for (int i = 0; i < lista.size(); i++) {
            tabla.setValueAt(lista.get(i).getCod_nac(), i, 0);
            tabla.setValueAt(lista.get(i).getNom_nac(), i, 1);
            tabla.setValueAt(lista.get(i).getNom_pais(), i, 2);
        }
        TablaNacionalidad.setModel(tabla);
        TablaNacionalidad.getColumnModel().getColumn(0).setMaxWidth(0);
        TablaNacionalidad.getColumnModel().getColumn(0).setMinWidth(0);
        TablaNacionalidad.getColumnModel().getColumn(0).setPreferredWidth(0);
    }
    
    
    private void ComboPaises(){
       DefaultComboBoxModel combo = new DefaultComboBoxModel();
        Control_Autores ca=new Control_Autores();
        List<PaisAutor> lista=ca.comboPais();
        for(PaisAutor P:lista){
            P = new PaisAutor(P.getCod_pais(), P.getNom_pais());
            combo.addElement(P);
        }
        ComboPais.setModel(combo);
    }
    
