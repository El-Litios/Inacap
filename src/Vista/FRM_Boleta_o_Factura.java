    public void LlenarTabla(String Dato){
    tabla.setColumnCount(0);
        tabla.addColumn("Codigo");
        tabla.addColumn("Folio");
        tabla.addColumn("Precio Neto");
        tabla.addColumn("Iva");
        tabla.addColumn("Total");
        tabla.addColumn("Fecha/Hora Emitida");
        tabla.addColumn("Tipo");
        tabla.addColumn("Metodo de Pago");
        tabla.addColumn("hora");
        tabla.addColumn("fecha");
        List<BoletaOfactura> lista=crbf.ListarBoF(Dato);
        tabla.setNumRows(lista.size());
        registros.setText("CANTIDAD DE REGISTROS= "+lista.size());
        for (int i = 0; i < lista.size(); i++) {
            tabla.setValueAt(lista.get(i).getCod_bof(), i, 0);
            tabla.setValueAt(lista.get(i).getFolio(), i, 1);
            tabla.setValueAt(lista.get(i).getPrecio_neto(), i, 2);
            tabla.setValueAt(lista.get(i).getCostoiva(), i, 3);
            tabla.setValueAt(lista.get(i).getPrecio_iva(), i, 4);
            tabla.setValueAt(lista.get(i).getFecha()+" / "+lista.get(i).getHora(), i, 5);
            tabla.setValueAt(lista.get(i).getNom_tipobf(), i, 6);
            tabla.setValueAt(lista.get(i).getNom_metod(), i, 7);
            tabla.setValueAt(lista.get(i).getHora(), i, 8);
            tabla.setValueAt(lista.get(i).getFecha(), i, 9);
        }
        TablaBoF.setModel(tabla);
        TablaBoF.getColumnModel().getColumn(0).setMaxWidth(0);
        TablaBoF.getColumnModel().getColumn(0).setMinWidth(0);
        TablaBoF.getColumnModel().getColumn(0).setPreferredWidth(0);
        
        TablaBoF.getColumnModel().getColumn(8).setMaxWidth(0);
        TablaBoF.getColumnModel().getColumn(8).setMinWidth(0);
        TablaBoF.getColumnModel().getColumn(8).setPreferredWidth(0);
        
        TablaBoF.getColumnModel().getColumn(9).setMaxWidth(0);
        TablaBoF.getColumnModel().getColumn(9).setMinWidth(0);
        TablaBoF.getColumnModel().getColumn(9).setPreferredWidth(0);
    }
    
    private void ComboTipo(){
       DefaultComboBoxModel combo = new DefaultComboBoxModel();
        Control_Boleta_o_Factura c=new Control_Boleta_o_Factura();
        List<TipoBF> lista=c.comboBoxTipoBoF();
        for(TipoBF bf:lista){
            bf = new TipoBF(bf.getCod_tipobf(), bf.getNom_tipobf());
            combo.addElement(bf);
        }
        ComboTipo.setModel(combo);
    }
    
    private void ComboMetodo(){
       DefaultComboBoxModel combo = new DefaultComboBoxModel();
        Control_Boleta_o_Factura c=new Control_Boleta_o_Factura();
        List<MetodoPago> lista=c.comboBoxMetodoP();
        for(MetodoPago bf:lista){
            bf = new MetodoPago(bf.getCod_metod(), bf.getNom_metod());
            combo.addElement(bf);
        }
        ComboMetodoP.setModel(combo);
    }
    
