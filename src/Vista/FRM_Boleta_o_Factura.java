    private void EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarActionPerformed
        if (filaBoF>-1) {
            codBoF=Integer.parseInt(String.valueOf(TablaBoF.getValueAt(filaBoF, 0)));
            Folio.setText(String.valueOf(TablaBoF.getValueAt(filaBoF, 1)));
            Precio.setText(String.valueOf(TablaBoF.getValueAt(filaBoF, 2)));
            Fecha.setText(String.valueOf(TablaBoF.getValueAt(filaBoF, 9)));
            Hora.setText(String.valueOf(TablaBoF.getValueAt(filaBoF, 8)));
            sswitch=1;
            Panel.setSelectedIndex(1);
        } else {
           JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UNA FILA","ADVERTENCIA",JOptionPane.OK_OPTION+JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_EditarActionPerformed

    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        if (filaBoF>-1) {
            int respuesta=JOptionPane.showConfirmDialog(this, "¿REALMENTE DESEA ELEMINAR EL REGISTRO? NO VOLVERA A RECUPERAR EL REGISTRO...");
            if (respuesta==0) {
                crbf.EliminarBoF(Integer.parseInt(String.valueOf(TablaBoF.getValueAt(filaBoF, 0))));
                LlenarTabla("");
                JOptionPane.showMessageDialog(this, "ARRIENDO ELIMINADO", "LA BIBLIOTECA",JOptionPane.OK_OPTION+JOptionPane.INFORMATION_MESSAGE);
                filaBoF=-1;
            }
        } else {
           JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UNA FILA","ADVERTENCIA",JOptionPane.OK_OPTION+JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_EliminarActionPerformed

