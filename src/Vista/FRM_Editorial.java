    public void LlenarTablaEditoriales(String Dato){
    tabla.setColumnCount(0);
        tabla.addColumn("Codigo");
        tabla.addColumn("Nombre de Editorial");
        List<Editorial> lista=crl.listaEdit(Dato);
        tabla.setNumRows(lista.size());
        RegistrosEdit.setText("NÂ° EDITORIALES DISPONIBLES= "+lista.size());
        for (int i = 0; i < lista.size(); i++) {
            tabla.setValueAt(lista.get(i).getCod_edit(), i, 0);
            tabla.setValueAt(lista.get(i).getNom_edit(), i, 1);
        }
        TablaEditoriales.setModel(tabla);
        TablaEditoriales.getColumnModel().getColumn(0).setMaxWidth(0);
        TablaEditoriales.getColumnModel().getColumn(0).setMinWidth(0);
        TablaEditoriales.getColumnModel().getColumn(0).setPreferredWidth(0);
    } 
    
