    private void EliminarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarEActionPerformed
        if (filaE>-1) {
            int respuesta=JOptionPane.showConfirmDialog(this, "Â¿REALMENTE DESEA ELEMINAR EL REGISTRO? NO VOLVERA A RECUPERAR EL REGISTRO..."+" "
                +String.valueOf(TablaEditoriales.getValueAt(filaE, 1)),"ADVERTENCIA",JOptionPane.YES_NO_OPTION+JOptionPane.WARNING_MESSAGE);
            if (respuesta==0) {
                crl.eliminarEditoriales(Integer.parseInt(String.valueOf(TablaEditoriales.getValueAt(filaE, 0))));
                LlenarTablaEditoriales("");
                JOptionPane.showMessageDialog(this, "LIBRO ELIMINADO", "LA BIBLIOTECA",JOptionPane.OK_OPTION+JOptionPane.INFORMATION_MESSAGE);
                filaE=-1;
            }
        } else {
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UNA FILA","ADVERTENCIA",JOptionPane.OK_OPTION+JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_EliminarEActionPerformed

