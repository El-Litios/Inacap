    private void EditarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarEActionPerformed
        if (filaE>-1) {
            codE=Integer.parseInt(String.valueOf(TablaEditoriales.getValueAt(filaE, 0)));
            Nom.setText(String.valueOf(TablaEditoriales.getValueAt(filaE, 1)));
            sswitch=1;
            PanelEditoriales.setSelectedIndex(1);
        } else {
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UNA FILA","ADVERTENCIA",JOptionPane.OK_OPTION+JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_EditarEActionPerformed

