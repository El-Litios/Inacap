    private void GuardarEditorialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarEditorialActionPerformed
        if (Nom.getText().equals("") ){
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR EL DATO", "La biblioteca",JOptionPane.OK_OPTION+JOptionPane.INFORMATION_MESSAGE);
            Nom.grabFocus();
        }else{
            if (sswitch==0) crl.InsertarEdit(Nom.getText());

            else crl.EditarEditoriales(codE, Nom.getText());

            
        }
        Nom.setText("");
        LlenarTablaEditoriales("");
        sswitch=0;
        PanelEditoriales.setSelectedIndex(0);
        JOptionPane.showMessageDialog(this, "OPERACION REALIZADA", "LA BIBLIOTECA",JOptionPane.OK_OPTION+JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_GuardarEditorialActionPerformed


